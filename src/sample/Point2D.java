package sample;

public class Point2D {

    int x;
    int y;
    int getX() {
        return x;
    }
    void setX(int x) {
        this.x = x;
    }
    int getY() {
        return y;
    }
    void setY(int y) {
        this.y = y;
    }
    public Point2D(){}
    public Point2D(int x, int y){this.x=x; this.y=y;}

}
